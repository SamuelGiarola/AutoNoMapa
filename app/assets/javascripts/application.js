function OpenSel(option) {

    if (option == 1) {
        document.getElementById('first_step').style.display="block";
        document.getElementById('secound_step').style.display="none";
        document.getElementById('third_step').style.display="none";
        document.getElementById('fourth_step').style.display="none";
        $('#step1').attr({'src':'/imgs/icons/icon1.png'});
        $('#step2').attr({'src':'/imgs/icons/icon22.png'});
        $('#step3').attr({'src':'/imgs/icons/icon33.png'});
        $('#step4').attr({'src':'/imgs/icons/icon44.png'});
    }else if (option == 2) {
        document.getElementById('first_step').style.display="none";
        document.getElementById('secound_step').style.display="block";
        document.getElementById('third_step').style.display="none";
        document.getElementById('fourth_step').style.display="none";
        $('#step1').attr({'src':'/imgs/icons/icon1.png'});
        $('#step2').attr({'src':'/imgs/icons/icon2.png'});
        $('#step3').attr({'src':'/imgs/icons/icon33.png'});
        $('#step4').attr({'src':'/imgs/icons/icon44.png'});
    }else if (option == 3) {
        document.getElementById('first_step').style.display="none";
        document.getElementById('secound_step').style.display="none";
        document.getElementById('third_step').style.display="block";
        document.getElementById('fourth_step').style.display="none";
        $('#step1').attr({'src':'/imgs/icons/icon1.png'});
        $('#step2').attr({'src':'/imgs/icons/icon2.png'});
        $('#step3').attr({'src':'/imgs/icons/icon3.png'});
        $('#step4').attr({'src':'/imgs/icons/icon44.png'});
    }else {
        document.getElementById('first_step').style.display="none";
        document.getElementById('secound_step').style.display="none";
        document.getElementById('third_step').style.display="none";
        document.getElementById('fourth_step').style.display="block";
        $('#step1').attr({'src':'/imgs/icons/icon1.png'});
        $('#step2').attr({'src':'/imgs/icons/icon2.png'});
        $('#step3').attr({'src':'/imgs/icons/icon3.png'});
        $('#step4').attr({'src':'/imgs/icons/icon4.png'});
    }
}
